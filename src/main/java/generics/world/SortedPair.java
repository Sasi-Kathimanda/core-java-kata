package generics.world;

public class SortedPair {
    private final int left;
    private final int right;

    public SortedPair(int left, int right) {
        this.left = left;
        this.right = right;
    }

    public int getLeft() {
        return left;
    }

    public int getRight() {
        return right;
    }
}
